#membership operator
#in

a = "gokavaramsai@gmail.com"
print("@" in a)

a = "gokavaramsai$gmail.com"
print("$" in a)

attendances = ( "dhanunnjai", "sai", "sundhar")
print( "sai" in attendances )

attendances = ( "dhanunnjai", "sai", "sundhar")
a = "sweety"
